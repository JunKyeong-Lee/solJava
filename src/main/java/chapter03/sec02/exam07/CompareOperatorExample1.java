package chapter03.sec02.exam07;

public class CompareOperatorExample1 {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 10;
        System.out.println(num1 == num2);
        System.out.println(num1 != num2);
        System.out.println(num1 > num2);
        System.out.println(num1 <= num2);

        char char1 = 'A';
        char char2 = 'B';
        System.out.println(char1 < char2);
    }
}
